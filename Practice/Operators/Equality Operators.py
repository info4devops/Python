# Equality Operators
a=input('Enter a value:')
b=input('Enter b value:')
#type casting
a=int(a)
b=int(b)
print('value of a==b:',a==b)
print('value of a!=b:',a!=b)
print('Id of a:',id(a))
print('Id of b:',id(b))

print('value of a is b:',a is b)
print('value of a is not b:',a is not b)
print()
