# Equality Operators
a=input('Enter a value:')
b=input('Enter b value:')
#type casting
a=bool(a)
b=bool(b)
print('After conversion,a=',a)
print('After conversion,b=',b)
'''x and y: if x evaluated as zero the returns x as out put
x evaluated as non zero the y as output'''

print('value of (a and b):',a and b)

'''x or y: if x evaluated as true (non zero) the returns x as out put
x evaluated as false (zero) the y as output'''

print('value of (a or b):',a or b)
print('value of not a:',not a)
print('value of not b:', not b)
