# decimal value
a=10
print('a:',a)
print('Type of a: ',type(a))

#binary representation
a1=0B1111
print('a1:',a1)
print('Type of a1: ',type(a1))
print('Decimal value of a1:',a1)

#Octal representation
a2=0O123
print('a2:',a2)
print('Type of a2: ',type(a2))
print('Decimal value of a2:',a2)

#Hexa Decimal representation
a3=0X123
print('a2:',a3)
print('Type of a3: ',type(a3))
print('Decimal value of a3:',a3)


