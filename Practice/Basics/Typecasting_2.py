#Type casting float to others
#Type casting: float to int

b=10.2354
n1=int(b)
print('int value of b:',n1)
print('type of n1:',type(n1))
print()

#Type casting: float to bool
f1=1.0
bool_f1=bool(f1)
print('Bool value of f1:',bool_f1)
print('Type of bool_f1: ',type(bool_f1))
print()

f2=0.0
bool_f2=bool(f2)
print('Bool value of f1:',bool_f2)
print('Type of bool_f2: ',type(bool_f2))
print()

