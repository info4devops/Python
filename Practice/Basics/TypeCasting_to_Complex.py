#Type casting to complex
# From int to complex
a=10 # Int value
n1=complex(a)
print('Complex of n1:',n1)
print('Type of n1:',type(n1))
print()

# From float to complex
a2=11.2 # float value
n2=complex(a2)
print('Complex of n2:',n2)
print('Type of n2:',type(n2))
print()

# From bool to complex
a3=True # Bool
n3=complex(a3)
print('Complex of n3:',n3)
print('Type of n3:',type(n3))
print()

a4=False # Bool
n4=complex(a4)
print('Complex of n4:',n4)
print('Type of n4:',type(n4))
print()

# From str to complex
a5='10' # str
n5=complex(a5)
print('Complex of n5:',n5)
print('Type of n5:',type(n5))
print()

# From binary to complex
a6=0b10101 # binary
n6=complex(a6)
print('Complex of n6:',n6)
print('Type of n6:',type(n6))
print()

# From octal to complex
a7=0o347 # octal
n7=complex(a7)
print('Complex of n7:',n7)
print('Type of n7:',type(n7))
print()

# From hexa decimal to complex
a8=0xa1b2f # hexa decimal
n8=complex(a8)
print('Complex of n8:',n8)
print('Type of n8:',type(n8))
print()
