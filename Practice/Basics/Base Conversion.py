# Decimal to other forms
#1.Conversion of decimal number to binary [0b/0B]
a=25
bin_a=bin(a)
print('Decimal value of a: ',a)
print('Binary value of a:',bin_a)

#2.Conversion of decimal number to octal [0o/0O]
oct_a=oct(a)
print('Octal value of a:',oct_a)

#3.Conversion of decimal number to Hexa decimal [0o/0O]
hex_a=hex(a)
print('Hexa-Decimal value of a:',hex_a)

# Binary to other forms
b=0b10101
print('Binary value of b: ',b)
#Conversion of binary number to Octal
oct_b=oct(b)
print('Octal value of b:',oct_b)
#Conversion of binary number to Hexa decimal
hex_b=hex(b)
print('Hexa-decimal value of b:',hex_b)


# Hexa decimal to other forms
h=0XAf12
print('Decimal value of h',h)
bin_h=bin(h)
print('Binary value of h:',bin_h)
oct_h=oct(h)
print('Octal value of h:',oct_h)
