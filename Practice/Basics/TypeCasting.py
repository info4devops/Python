# Type casting int to other forms
#Type casting: Int to float
a=5
n=float(a)
print('float value of a:',n)
print('type of n:',type(n))
print()

#Type Casting: int to bool
b1=1
n2=bool(b1)
print('bool value of b1:',n2)
print('type of n2:',type(n2))
print()
b2=0
n3=bool(b2)
print('bool value of b2:',n3)
print('type of n3:',type(n3))

#Type Casting: int to str
p=10
str_p=str(p)
print('str value of p:',str_p)
print('type of str_p:',type(str_p))

#Type Casting: int to complext
c=22
result_c=complex(c)
print('complex value of c:',result_c)
print('type of result_c:',type(result_c))



